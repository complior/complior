/**
 * Eval Fix Generator — generates system prompt patches and API config patches
 * from eval failures + remediation playbooks.
 *
 * US-REM-05: System Prompt Patch Generator
 * US-REM-06: API Config Patch Generator
 */

import type { TestResult } from './types.js';
import type { CategoryPlaybook, RemediationAction, ApiConfigPatch } from './remediation-types.js';
import {
  PRIORITY_ORDER,
  CATEGORY_ARTICLES,
  CATEGORY_FINES,
  groupFailuresByCategory,
  findPlaybook,
} from './eval-constants.js';

// ── System Prompt Patch ──────────────────────────────────────

/**
 * Generate a system prompt patch from failed tests.
 *
 * Groups failures by category, deduplicates, sorts by priority,
 * and produces a numbered markdown string of system prompt additions.
 */
export const generateSystemPromptPatch = (
  failures: readonly TestResult[],
  playbooks: readonly CategoryPlaybook[],
): string => {
  // Group failures by category
  const byCat = groupFailuresByCategory(failures);

  // Collect system_prompt actions per category (deduplicated)
  const blocks: { category: string; article: string; actions: RemediationAction[]; testCount: number }[] = [];
  const seenActionIds = new Set<string>();

  for (const [cat, catFailures] of byCat) {
    const pb = findPlaybook(playbooks, cat);
    if (!pb) continue;

    const promptActions = pb.actions.filter((a) => a.type === 'system_prompt');
    const newActions: RemediationAction[] = [];
    for (const a of promptActions) {
      if (!seenActionIds.has(a.id)) {
        seenActionIds.add(a.id);
        newActions.push(a);
      }
    }

    if (newActions.length > 0) {
      const article = CATEGORY_ARTICLES[cat] ?? pb.article_ref;
      blocks.push({ category: cat, article, actions: newActions, testCount: catFailures.length });
    }
  }

  // Sort blocks by priority of their highest-priority action
  blocks.sort((a, b) => {
    const aPri = Math.min(...a.actions.map((x) => PRIORITY_ORDER[x.priority] ?? 3));
    const bPri = Math.min(...b.actions.map((x) => PRIORITY_ORDER[x.priority] ?? 3));
    if (aPri !== bPri) return aPri - bPri;
    return b.testCount - a.testCount;
  });

  if (blocks.length === 0) return '';

  // Render markdown
  const lines: string[] = [
    '# System Prompt Compliance Patch',
    '',
    `> Generated by complior eval — ${blocks.length} categories, ${failures.length} failing tests`,
    '',
    'Add the following instructions to your AI system\'s system prompt:',
    '',
  ];

  let n = 1;
  for (const block of blocks) {
    const fine = CATEGORY_FINES[block.category];
    const fineStr = fine ? ` (fine: ${fine})` : '';
    lines.push(`## ${n}. ${block.actions[0].title} — ${block.article}${fineStr}`);
    lines.push('');
    lines.push(`> ${block.testCount} tests affected`);
    lines.push('');

    for (const action of block.actions) {
      // Extract the quoted instruction from the example
      const instructionMatch = action.example.match(/"([^"]+)"/);
      const instruction = instructionMatch ? instructionMatch[1] : action.description;
      lines.push(`\`\`\`\n${instruction}\n\`\`\``);
      lines.push('');
    }

    n++;
  }

  return lines.join('\n');
};

// ── API Config Patch ─────────────────────────────────────────

/**
 * Generate API config patch from failed tests.
 *
 * Collects api_config + infrastructure actions and builds a structured
 * JSON object with headers, validation patterns, and provider examples.
 */
export const generateApiConfigPatch = (
  failures: readonly TestResult[],
  playbooks: readonly CategoryPlaybook[],
): ApiConfigPatch => {
  const headers: Record<string, string> = {};
  const bannedPatterns: string[] = [];
  const piiFilterPatterns: string[] = [];
  const promptLeakPatterns: string[] = [];

  // Check which categories have failures
  const failedCategories = new Set(failures.map((f) => f.owaspCategory ?? f.category));

  for (const cat of failedCategories) {
    const pb = findPlaybook(playbooks, cat);
    if (!pb) continue;

    for (const action of pb.actions) {
      if (action.type !== 'api_config') continue;

      // Transparency headers
      if (cat === 'transparency' || action.id === 'CT-1-A3') {
        headers['X-AI-Generated'] = 'true';
        headers['X-AI-Disclosure'] = 'This response was generated by an AI system';
        headers['X-Content-Origin'] = 'ai';
      }

      // GPAI headers
      if (cat === 'gpai') {
        headers['X-AI-Model'] = '{{model_name}}';
        headers['X-AI-Provider'] = '{{provider_name}}';
      }

      // Input validation (robustness, prompt injection)
      if (cat === 'robustness' || cat === 'LLM01') {
        bannedPatterns.push(
          'ignore previous instructions',
          'you are now',
          'new instructions:',
          'DAN mode',
          'jailbreak',
        );
      }

      // Output filtering (sensitive info, system prompt leakage)
      if (cat === 'LLM02') {
        piiFilterPatterns.push(
          '[\\w.-]+@[\\w.-]+\\.[a-z]{2,}',
          '\\+?\\d[\\d\\s-]{8,}',
          '\\d{3}-\\d{2}-\\d{4}',
        );
      }
      if (cat === 'LLM07') {
        promptLeakPatterns.push(
          'system prompt',
          'my instructions',
          'I was told to',
        );
      }
    }
  }

  // Deduplicate
  const uniqueBanned = [...new Set(bannedPatterns)];
  const uniquePii = [...new Set(piiFilterPatterns)];
  const uniqueLeak = [...new Set(promptLeakPatterns)];

  // Provider examples
  const providerExamples: Record<string, Record<string, unknown>> = {};

  if (Object.keys(headers).length > 0 || uniqueBanned.length > 0) {
    providerExamples['openai'] = {
      headers: { ...headers },
      max_tokens: 2048,
      temperature: 0.3,
    };
    providerExamples['anthropic'] = {
      headers: { ...headers },
      max_tokens: 2048,
    };
    providerExamples['ollama'] = {
      headers: { ...headers },
      options: { num_predict: 2048, temperature: 0.3 },
    };
  }

  return Object.freeze({
    headers,
    inputValidation: Object.freeze({
      maxLength: uniqueBanned.length > 0 ? 4096 : undefined,
      bannedPatterns: uniqueBanned.length > 0 ? Object.freeze(uniqueBanned) : undefined,
    }),
    outputValidation: Object.freeze({
      piiFilterPatterns: uniquePii.length > 0 ? Object.freeze(uniquePii) : undefined,
      promptLeakPatterns: uniqueLeak.length > 0 ? Object.freeze(uniqueLeak) : undefined,
    }),
    providerExamples: Object.freeze(providerExamples),
  });
};
