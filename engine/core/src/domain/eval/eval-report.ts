/**
 * Eval Report — generates a markdown report from an EvalResult.
 */

import type { EvalResult, CategoryScore } from './types.js';
import { CATEGORY_META } from './types.js';

/** Generate a full markdown eval report. */
export const generateEvalReport = (result: EvalResult): string => {
  const lines: string[] = [];

  lines.push(`# Complior Eval Report`);
  lines.push('');
  lines.push(`**Target:** ${result.target}`);
  lines.push(`**Tier:** ${result.tier}`);
  lines.push(`**Date:** ${result.timestamp}`);
  lines.push(`**Duration:** ${(result.duration / 1000).toFixed(1)}s`);
  if (result.agent) lines.push(`**Agent:** ${result.agent}`);
  lines.push('');

  // Overall score
  lines.push(`## Overall Score: ${result.overallScore}/100 (${result.grade})`);
  lines.push('');
  if (result.criticalCapped) {
    lines.push('> **WARNING:** Score capped due to critical category failure.');
    lines.push('');
  }
  lines.push(`| Metric | Value |`);
  lines.push(`|--------|-------|`);
  lines.push(`| Total tests | ${result.totalTests} |`);
  lines.push(`| Passed | ${result.passed} |`);
  lines.push(`| Failed | ${result.failed} |`);
  lines.push(`| Errors | ${result.errors} |`);
  lines.push('');

  // Security score
  if (result.securityScore !== undefined) {
    lines.push(`## Security Score: ${result.securityScore}/100 (${result.securityGrade})`);
    lines.push('');
  }

  // Category breakdown
  lines.push('## Category Breakdown');
  lines.push('');
  lines.push('| Category | CT | Score | Grade | Pass/Fail |');
  lines.push('|----------|-----|-------|-------|-----------|');

  for (const cs of result.categories) {
    const meta = CATEGORY_META.find((m) => m.id === cs.category);
    const label = meta?.label ?? cs.category;
    const ctId = meta?.ctId ?? '';
    lines.push(`| ${label} | ${ctId} | ${cs.score} | ${cs.grade} | ${cs.passed}/${cs.total} |`);
  }
  lines.push('');

  // Failed tests detail
  const failed = result.results.filter((r) => r.verdict === 'fail');
  if (failed.length > 0) {
    lines.push('## Failed Tests');
    lines.push('');
    for (const f of failed.slice(0, 50)) { // Cap at 50 for readability
      lines.push(`### ${f.testId} — ${f.name}`);
      lines.push(`- **Category:** ${f.category}`);
      lines.push(`- **Method:** ${f.method}`);
      lines.push(`- **Reasoning:** ${f.reasoning}`);
      lines.push('');
    }
    if (failed.length > 50) {
      lines.push(`_...and ${failed.length - 50} more failed tests._`);
      lines.push('');
    }
  }

  // Footer
  lines.push('---');
  lines.push(`Generated by Complior v8 eval engine.`);

  return lines.join('\n');
};

/** Generate a compact one-line summary suitable for CLI output. */
export const generateEvalSummary = (result: EvalResult): string => {
  const parts = [
    `Score: ${result.overallScore}/100 (${result.grade})`,
    `Pass: ${result.passed}/${result.totalTests}`,
  ];
  if (result.securityScore !== undefined) {
    parts.push(`Security: ${result.securityScore}/100`);
  }
  if (result.criticalCapped) {
    parts.push('CAPPED');
  }
  return parts.join(' | ');
};

/** Generate per-category score summary for CLI table output. */
export const generateCategoryTable = (categories: readonly CategoryScore[]): string => {
  const lines: string[] = [];
  lines.push('Category           | Score | Grade | Pass/Total');
  lines.push('-------------------|-------|-------|----------');
  for (const cs of categories) {
    if (cs.total === 0) continue;
    const meta = CATEGORY_META.find((m) => m.id === cs.category);
    const label = (meta?.label ?? cs.category).padEnd(18);
    lines.push(`${label} | ${String(cs.score).padStart(5)} | ${cs.grade.padStart(5)} | ${cs.passed}/${cs.total}`);
  }
  return lines.join('\n');
};
